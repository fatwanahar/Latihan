public class App {
    public static void main(String[] args) throws Exception {
        //integer
        int a=4;
        System.out.println("nilai int a=" + a);
        a++;
        System.out.println("nilai a++  " + a);
        System.out.println("nilai maksimum dari integer = " + Integer.MAX_VALUE);
        System.out.println("nilai maksimum dari integer = " + Integer.MIN_VALUE);
        //short
        short b=5;
        System.out.println("nilai short b=" + b);
        System.out.println("nilai maksimum dari short = " + Short.MAX_VALUE);
        System.out.println("nilai maksimum dari short = " + Short.MIN_VALUE);
        //long
        long c=100l;
        System.out.println("nilai long c=" + c);
        System.out.println("nilai maksimum dari long = " + Long.MAX_VALUE);
        System.out.println("nilai maksimum dari long = " + Long.MIN_VALUE);
        System.out.println("nilai maksimum dari long = " + Long.SIZE + "bit");
        //float
        float d=10.5f;
        System.out.println("nilai float d=" + d);
        System.out.println("nilai maksimum dari float = " + Float.MAX_VALUE);
        System.out.println("nilai maksimum dari float = " + Float.MIN_VALUE);
        //double
        double e=50.5d;
        System.out.println("nilai double e=" + e);
        System.out.println("nilai maksimum dari double = " + Double.MAX_VALUE);
        System.out.println("nilai maksimum dari double = " + Double.MIN_VALUE);
        
    }
}
